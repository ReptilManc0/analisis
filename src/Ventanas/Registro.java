    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        //Creacion del objeto de clase Usuarios "user"
        Usuarios user = new Usuarios();

        //Recoleccion y asignacion de datos
        user.nombre =jTextNombre.getText();
        user.apellidos=jTextApellidos.getText();
        user.username=jTextUsuario.getText();
        user.password=jPasswordField1.getText();
        user.contraconfi=jPasswordField2.getText();

        //Obtener fecha
        user.ObtenerFecha();        
        //Invocacion del metodo agregar usuario
        if (user.password.equals(user.contraconfi)) {
        user.AgregarUsuario();
        this.dispose();
        InicioDeSesion ini = new InicioDeSesion();
        ini.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Las contrase침as no coinciden");
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

