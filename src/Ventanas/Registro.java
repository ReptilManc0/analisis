    class RoundedPanel extends JPanel{
            private Color backgroundColor;
            private int cornerRadius = 15;
            public RoundedPanel(LayoutManager layout, int radius) {
                super(layout);
                cornerRadius = radius;
                setOpaque(false);
            }
            public RoundedPanel(LayoutManager layout, int radius, Color bgColor) {
                super(layout);
                cornerRadius = radius;
                backgroundColor = bgColor;
                setOpaque(false);
            }
            public RoundedPanel(int radius) {
                super();
                cornerRadius = radius;
                setOpaque(false);

            }
            public RoundedPanel(int radius, Color bgColor) {
                super();
                cornerRadius = radius;
                backgroundColor = bgColor;
                setOpaque(false);
            }


            @Override
          protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D graphics = (Graphics2D) g.create();
            graphics.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

            int width = getWidth();
            int height = getHeight();

            // Pintar el fondo del panel
            if (backgroundColor != null) {
                graphics.setColor(backgroundColor);
            } else {
                graphics.setColor(getBackground());
            }
            graphics.fillRect(0, 0, width, height);

            // Crear una forma rectangular con esquinas redondeadas
            RoundRectangle2D roundRect = new RoundRectangle2D.Double(0, 0, width - 1, height - 1, cornerRadius, cornerRadius);

            // Crear un Shape para el 치rea de recorte
            Area clipArea = new Area(new Rectangle(0, 0, width, height));
            clipArea.subtract(new Area(roundRect));

            // Establecer la transparencia para el fondo de las esquinas recortadas
            graphics.setComposite(AlphaComposite.getInstance(AlphaComposite.CLEAR, 0.0f));
            graphics.fill(clipArea);

            graphics.dispose();
        }    
    }
